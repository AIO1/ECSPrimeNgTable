/**
 * Enum representing the vertical alignment of content within a table cell.
 * 
 * @remarks
 * Determines how the content inside a cell is positioned vertically relative to the cell's height.
 */
export enum DataAlignVertical {
    /** Align content to the top of the cell. */
    Top,

    /** Align content to the vertical middle of the cell. */
    Middle,

    /** Align content to the bottom of the cell. */
    Bottom
}