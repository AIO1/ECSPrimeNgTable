/**
 * Enum representing the horizontal alignment of content within a table cell.
 * 
 * @remarks
 * Determines how the content inside a cell is positioned horizontally relative to the cell's width.
 */
export enum DataAlignHorizontal {
    /** Align content to the left of the cell. */
    Left,

    /** Align content to the center of the cell. */
    Center,

    /** Align content to the right of the cell. */
    Right
}